package com.pboreg;

public class IFelse {

    public static void main(String[] args){

        // if statement / percabangan
        int data = 5;
        System.out.println("nilai = " + data);

        // ini adalah cabangnya

        if (data == 5){
            System.out.println("Ini adalah cabang");

        } else{
            System.out.println("Salah jalan bos");
        }

        System.out.println("selesai");


    }
}
